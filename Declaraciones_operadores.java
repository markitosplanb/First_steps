
public class Declaraciones_operadores {

	
	public static void main(String[] args) {
		
		double a=7; // Declaración e iniciación de variable "a"
		
		double b=8; // Declaración e iniciación de variable "b"
		
		double c; // Declaración de varaiable "c"
		
		c=a/b; // Operador (+iniciación) "a/b=c", usando "double" para que el resultado incluya decimales
		
		c-=6;
		
		//c-=6
		
		System.out.println(c);
		
		

	}

}
